    public static void CargaParametros(){
        List<ClsClave> Parametros = (new ClsOperaClave()).ConsultaLista("1000", "", "");
        if (!Parametros.isEmpty()){
            Host = Parametros.stream().filter(u -> u.SecuencialDeClave() == 3).collect(Collectors.toList()).get(0).NombreDeClave();
            Host1 = Parametros.stream().filter(u -> u.SecuencialDeClave() == 3).collect(Collectors.toList()).get(0).Descripcion();
            Puerto = Parametros.stream().filter(u -> u.SecuencialDeClave() == 5).collect(Collectors.toList()).get(0).NombreDeClave();
            Puerto1 = Parametros.stream().filter(u -> u.SecuencialDeClave() == 5).collect(Collectors.toList()).get(0).Descripcion();
            CorreoServidor = Parametros.stream().filter(u -> u.SecuencialDeClave() == 6).collect(Collectors.toList()).get(0).NombreDeClave();
            ContraseñaServidor = Parametros.stream().filter(u -> u.SecuencialDeClave() == 7).collect(Collectors.toList()).get(0).NombreDeClave();
        }
    }
