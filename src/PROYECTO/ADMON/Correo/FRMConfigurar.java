    private void btnProbarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProbarActionPerformed
        // TODO add your handling code here:
        ClsEnviarCorreo Correo = new ClsEnviarCorreo();
        Correo.ConfiguracionPrueba(txtCorreoServidor.getText(),Desifrado(txtContraseñaServidor.getPassword()), txtPuerto.getText(), txtServidor.getText());
        String directorio = System.getProperty("user.dir") + "/src/Content/Correos/CorreoDeSistema.html";
        if(ClsEnviarCorreo.EnviarCorreo(txtPara.getText(), txtAsunto.getText(),"Correo de Prueba",txtAsunto.getText(),txtMensaje.getText(),directorio)){
            lblIcono.setIcon(ColocaIcono(getRutaIconoCorrecto(),30,30,(int) CENTER_ALIGNMENT));
            lblIcono.setVisible(true);
            lblCorrecto.setText("Enviado");
        }else{
            lblIcono.setIcon(ColocaIcono(getRutaIconoIncorrecto(),30,30,(int) CENTER_ALIGNMENT));
            lblIcono.setVisible(true);
            lblCorrecto.setText("Error de envio");
        }
    }//GEN-LAST:event_btnProbarActionPerformed

