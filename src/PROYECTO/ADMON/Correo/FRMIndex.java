    private void tblCorreosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblCorreosMouseClicked
        // TODO add your handling code here:
        if(evt.getClickCount() == 2){
            int row = tblCorreos.rowAtPoint(evt.getPoint());
            Class[] Tipos = {ClsCorreo.class};
            ClsCorreo Correo = new ClsCorreo();
            Correo.NumeroDeCorreo(Long.parseLong(tblCorreos.getValueAt(row, 0).toString()));
            Correo.CveTipoDeCorreo(ClsCargaCombos.CambiaClave("5","",tblCorreos.getValueAt(row, 1).toString()));
            Correo.Correo(tblCorreos.getValueAt(row, 2).toString());
            Correo.CveEstatus(ClsCargaCombos.CambiaClave("1","",tblCorreos.getValueAt(row, 3).toString()));
            Correo.TextoTipoDeCorreo(tblCorreos.getValueAt(row, 1).toString());
            Correo.TextoEstatus(tblCorreos.getValueAt(row, 3).toString());
            Object[] Parametros = {Correo};
            PROYECTO.DESKTOP.DesktopMain.AbrirPantalla("Correo.FRMDetalle", Tipos, Parametros, false);
            if (Sesion.CierreVentana(NumeroDePantalla)){
                this.dispose();
            }
        }
    }//GEN-LAST:event_tblCorreosMouseClicked

