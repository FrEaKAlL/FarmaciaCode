    private Object[][] CargaCorreosEnTabla() {
        String Filtro = "";
        List<ClsCorreo> lstCorreo = (new ClsOperaCorreo()).ConsultaLista("","", Filtro);
        Object Perfiles[][] = new Object[lstCorreo.size()][ClsCorreo.RelacionesCorreo.CamposQueSeMuestranEnTabla.size()];
        try{
        for (int x = 0; x < lstCorreo.size(); x++){
            for (int y = 0; y < ClsCorreo.RelacionesCorreo.CamposQueSeMuestranEnTabla.size(); y ++){
                Method getElemento = lstCorreo.get(x).getClass().getMethod(ClsCorreo.RelacionesCorreo.CamposQueSeMuestranEnTabla.get(y).Entidad());
                Perfiles[x][y] = getElemento.invoke(lstCorreo.get(x));
            }
        }
        }catch(IllegalAccessException | IllegalArgumentException | NoSuchMethodException | SecurityException | InvocationTargetException ex){
            System.out.println(ex.getMessage());
        }
        return Perfiles;
    }
    private String[] Titulos(){
        String Titulos[] = new String[ClsCorreo.RelacionesCorreo.CamposQueSeMuestranEnTabla.size()];
        for (int i = 0; i < ClsCorreo.RelacionesCorreo.CamposQueSeMuestranEnTabla.size(); i++){
            Titulos[i] = ClsCorreo.RelacionesCorreo.CamposQueSeMuestranEnTabla.get(i).Texto();
        }
        return Titulos;
    }
